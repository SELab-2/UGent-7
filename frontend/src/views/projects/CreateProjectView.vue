                        <div class="field col">
                            <label for="maxScore">{{ t('views.projects.max_score') }}</label>
                            <InputNumber id="maxScore" class="w-full" v-model="form.maxScore" :min="1" />
                            <ErrorMessage :field="v$.maxScore" />
