                        </div>
                    </div>

                    <div class="grid">
                        <!-- Project description -->
                        <div class="field col">
                            <label for="projectDescription">
                                {{ t('views.projects.description') }}
                            </label>
                            <Editor id="projectDescription" class="w-full" v-model="form.description" />
                        </div>
                    </div>

                    <div class="grid">
                        <!-- Start date of the project -->
